#include <unistd.h>

void	ft_putpuzzle(char **puzzle)
{
	int		x;
	int		y;
	char	nb;

	x = 1;
	y = 0;
	while (x < 10)
	{
		while (y < 9)
		{
			nb = puzzle[x][y];
			write(1, &nb, 1);
			write(1, " ", 1);
			y++;
		}
		write(1, "\n", 1);
		y = 0;
		x++;
	}
}
