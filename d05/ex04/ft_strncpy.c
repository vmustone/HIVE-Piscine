char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	if (*dest == '0' && *src == '0')
		return (0);
	while (*src && n--)
	{
		*dest = *src;
		src++;
		dest++;
	}
		*dest = '\0';
	return (0);
}
